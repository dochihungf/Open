using System.ComponentModel.DataAnnotations.Schema;
using Open.Core.SeedWork.Events;
using Open.Core.SeedWork.Interfaces;

namespace Open.Core.SeedWork;

public abstract class DomainEntity<TKey> : Entity<TKey>
{
    private readonly List<DomainEvent> _domainEvents = [];

    [NotMapped]
    public IReadOnlyCollection<DomainEvent> DomainEvents => _domainEvents.AsReadOnly();

    public void RegisterDomainEvent(DomainEvent domainEvent)
    {
        _domainEvents.Add(domainEvent);
    }

    public void RemoveDomainEvent(DomainEvent domainEvent)
    {
        _domainEvents.Remove(domainEvent);
    }

    public void ClearDomainEvents()
    {
        _domainEvents.Clear();
    }
}

